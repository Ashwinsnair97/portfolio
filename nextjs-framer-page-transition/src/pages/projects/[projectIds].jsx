const findNextProject = (current, allCards) => {
    let position = 0;
    allCards.map((element, index) => {
        if(element.title == current){
            if(index == (allCards.length - 1)){
                position = 0;
            }
            else{
                position = index+1
            }
        }
    })
    return allCards[position]
}